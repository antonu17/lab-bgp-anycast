print() {
    [ -n "$VERBOSE" ] && echo $1 || echo -n $1
}

do_ping() {
    [ -n "$VERBOSE" ] && TARGET="$HOSTNAME <---> $1 "
    ping -c 1 -W 0.1 $1 >/dev/null 2>&1 && print "${TARGET}." || >&2 print "${TARGET}x"
}

if [ "$1" == "-v" ]; then
    VERBOSE="1"
fi

for IP in "${IPV4_TARGETS[@]}"; do
    do_ping $IP
done

for IP in "${IPV6_TARGETS[@]}"; do
